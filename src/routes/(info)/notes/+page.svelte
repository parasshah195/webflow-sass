<div class="page-content">
  <div class="note">
    <h2>Notes</h2>
    <ul>
      <li>
        <b>Set <code>.scss</code> code element's <code>Visibility</code> to <code>Hidden</code></b> to
        ensure that the sass code doesn't output on the live site
      </li>
      <li>
        To use <code>Tab</code> key for traversing app elements, press <code>Escape</code> to focus out
        of the Editor
      </li>
      <li>
        This app can be used to write normal CSS too. Enjoy the code IDE benefits to write and test
        your custom CSS faster
      </li>
      <li>The compiled CSS is outputted in a minified format on the frontend</li>
      <li>
        To use keyboard in Webflow navigator when this app is open, click anywhere on canvas to take
        focus away from the app window.
      </li>
      <li>
        This app and the developer does not provide any support for CSS/Sass code that you may write
        here
      </li>
    </ul>
  </div>

  <div class="note">
    <h2>Error Actions</h2>
    <ul>
      <li>
        <p>
          <b>"Unable to locate current <code>.scss</code> element"</b> - This happens when either:
        </p>
        <ul>
          <li>
            The <code>.scss</code> element is not currently visible in the navigator - In this case,
            navigate to inside/outside a component where the <code>.scss</code> file resides.
            <b
              >If you see the current <code>.scss</code> in the navigator, the app should be able to
              update it.</b
            >
          </li>
          <li>
            The <code>.scss</code> element has been deleted - In this case, copy the code and start a
            new file
          </li>
        </ul>
      </li>
    </ul>
  </div>
</div>
